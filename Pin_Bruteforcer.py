    freq = round(1000000 / (int(keys[1], 16) * .241246))
    #print(f"Utilizando una frequencia de {freq}\n")

    milisecs = []
    for hex_value in keys:
        milisecs.append(round(1000000 * int(hex_value, 16) / freq))

